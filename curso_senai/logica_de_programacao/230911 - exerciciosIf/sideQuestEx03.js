let diaSemana = Number(prompt("Digite um numero da semana"))

switch (diaSemana) {
    case 1:
        alert("Você escolheu domingo")
        break;

    case 2:
        alert("Você escolheu segunda")
        break;

    case 3:
        alert("Você escolheu terça")
        break;

    case 4:
        alert("Você escolheu quarta")
        break;

    case 5:
        alert("Você escolheu quinta")
        break;

    case 6:
        alert("Você escolheu sexta")
        break;

    case 7:
        alert("Você escolheu sabado")
        break;
        
    default:
        alert("Escolha um número válido")
        break;
}